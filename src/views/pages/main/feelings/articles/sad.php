  <p><strong>Pratique o autocuidado:</strong> Dedique um tempo para cuidar de si mesmo. Priorize atividades que tragam conforto e bem-estar, como tomar um banho relaxante, ler um livro, ouvir música suave, meditar ou passear ao ar livre. O autocuidado é essencial para nutrir sua saúde mental e emocional.</p>

