  <p><strong>Não negligencie sua saúde física:</strong> Cuide do seu corpo, mesmo quando estiver se sentindo triste. Certifique-se de comer alimentos nutritivos, praticar exercícios físicos regulares e descansar adequadamente. O cuidado com o corpo também contribui para a sua saúde mental.</p>

